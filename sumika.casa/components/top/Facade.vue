      // 指定秒数ごとにカウンターの値を変更
      counterChange () {
        let number = 2
        const el = this.$refs.topFacadeShowcase

        el.appendChild(el.querySelector('.p-topFacadeShowcase__item').cloneNode(true))

        el.setAttribute('data-count', '1')
        setInterval(() => {
          if (number > 4) {
            number = 1
          } else {
            el.setAttribute('data-count', number.toString())
            number++
          }
        }, 5000)
      },
    }
